package n_June_18th_Tasks_For_While_Loop;
//Print the sum of first 10 natural numbers
public class Task4 {
    public static void main(String[] args) {
        int sum=0;
        for (int i=1; i<=10;i++){
            sum=sum+i;
        }
        System.out.print(sum);
    }
}
