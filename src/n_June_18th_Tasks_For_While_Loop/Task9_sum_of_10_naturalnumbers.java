package n_June_18th_Tasks_For_While_Loop;

//Calculate sum of first 10 natural numbers

public class Task9_sum_of_10_naturalnumbers {
    public static void main(String[] args) {
        int sum=0;
        int i=1;
        while(i<=10){
            sum=sum+i;
            i++;
        }
        System.out.println(sum);
    }
}
