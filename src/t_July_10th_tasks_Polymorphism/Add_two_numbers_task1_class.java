package t_July_10th_tasks_Polymorphism;

public class Add_two_numbers_task1_class {
    int add (int a, int b){
        return a+b;
    }
    int add (int a, int b, int c){
        return a+b+c;
    }
    double add(double a, int b){
        return a+b;
    }
    double add(double a, double b){
        return a+b;
    }
    
}
