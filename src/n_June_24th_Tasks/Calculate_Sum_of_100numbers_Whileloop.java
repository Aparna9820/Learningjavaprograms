package n_June_24th_Tasks;

public class Calculate_Sum_of_100numbers_Whileloop {
    public static void main(String[] args) {
        int sum=0;
        int i=1;
        while(i<=100){
            sum=sum+i;
            i++;
        }
        System.out.println(sum);
    }
}
