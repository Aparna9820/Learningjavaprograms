package n_June_24th_Tasks;

public class Inverted_Right_Triangle_ForLoop {
    public static void main(String[] args) {
        for(int i=5; i>=1;i--){
            for(int j=1; j<=i;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
