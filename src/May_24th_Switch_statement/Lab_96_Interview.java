package May_24th_Switch_statement;

public class Lab_96_Interview {
    public static void main(String[] args) {

        char code = 'C';
        switch (code) {
            default:
                System.out.println("Hellooooooo");
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}//o/p---->  Hellooooooo
