//Create a Java program that demonstrates various arithmetic and assignment operators.
//
//**Requirements:**
//- Use +, -, *, /, % operators
//- Demonstrate assignment operators (=, +=, -=, *=, /=)
//- Show the results of each operation
public class Question2_Arthimetic_Operations {
    public static void main(String[] args) {
        int a=10, b=4;
        System.out.println("Addition of 2 numbers: " +(a+b));
        System.out.println("Subtraction of 2 numbers: " +(a-b));
        System.out.println("Multiplication of 2 numbers: " +(a*b));
        System.out.println("Division of 2 numbers: " +(a/b));
        System.out.println("Modulus of 2 numbers: " +(a%b));

    }

}
